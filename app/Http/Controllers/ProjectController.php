    {
        return view('project.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreProjectRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validator = Validator::make(
            $request->all(),
            [
                'project_name' => 'required|max:255|min:2|unique:projects',
                'group_number' => 'required|integer|max:10|min:2',
                'student_number' => 'required|integer|max:10|min:2'
            ]
        );

        $request->flash();

        if ($validator->fails()) {
            return redirect()
                ->back()
                ->withErrors($validator);
        }

        $project = new Project;
        $project->project_name = $request->project_name;
        $project->group_number = $request->group_number;
        $project->student_number = $request->student_number;
        $project->save();

        return redirect()
            ->route('project_index')
            ->with('success_message', 'New project was successfully created.');
    }

